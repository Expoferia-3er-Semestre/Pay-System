    public boolean validar() {
        
        boolean error=false;
        if (txtcorreo.getText().isBlank()) {
            warningCorreo.setVisible(true);
            error=true;
        } else warningCorreo.setVisible(false);
        
        char[] charPassword=txtpassword.getPassword();
        String password=new String(charPassword);
        java.util.Arrays.fill(charPassword, ' ');
        if (password.isBlank()) {
            warningContrasena.setVisible(true);
            error=true;
        } else warningContrasena.setVisible(false);
        password=null;
        return error;
        
    }
    
    public void cancelar() {
        txtcorreo.setText("");
        warningCorreo.setVisible(false);
        txtpassword.setText("");
        warningContrasena.setVisible(false);
    }
    
    public void login() {
        EmpleadoDAO em=new EmpleadoDAO();
        if (!validar()) {
             Empleado empleado =em.buscarContrasena(txtcorreo.getText());
             
        char[] charPassword=txtpassword.getPassword();
        String password=new String(charPassword);
        java.util.Arrays.fill(charPassword, ' ');
        if (Password.verificarContrasena(password, empleado.getContrasena())) {
            password=null;
            JOptionPane.showMessageDialog(this, "Sesi칩n iniciada");
            this.dispose();
            // Si es true lo mandar치 a admin
            if (empleado.getRol()) {
                HomeAdmin escritorio=new HomeAdmin();
                escritorio.setVisible(true);
                escritorio.setLocationRelativeTo(null);
            } else {

            }

        }else {
            JOptionPane.showMessageDialog(this, "Correo o contrase침a incorrecta");
            intentos+=1;
        }
        }
             
    }
    
