    public boolean validar() {
        
        boolean error=false;
        if (txtcorreo.getText().isBlank()) {
            warningCorreo.setVisible(true);
            error=true;
        } else warningCorreo.setVisible(false);
        
        if (txtpassword.getText().isBlank()) {
            warningContrasena.setVisible(true);
            error=true;
        } else warningContrasena.setVisible(false);
        
        return error;
        
    }
    
    public void cancelar() {
        txtcorreo.setText("");
        warningCorreo.setVisible(false);
        txtpassword.setText("");
        warningContrasena.setVisible(false);
    }
    
    public void login() {
        EmpleadoDAO em=new EmpleadoDAO();
        if (!validar()) {
             String contrasenaH=em.buscarContrasena(txtcorreo.getText());
        
        if (Password.verificarContrasena(txtpassword.getText(), contrasenaH)) {
            JOptionPane.showMessageDialog(this, "Sesión iniciada");
            this.dispose();
            Escritorio escritorio=new Escritorio();
            escritorio.setVisible(true);
            escritorio.setLocationRelativeTo(null);
        }else JOptionPane.showMessageDialog(this, "Correo o contraseña incorrecta");
        }
             
    }
    
